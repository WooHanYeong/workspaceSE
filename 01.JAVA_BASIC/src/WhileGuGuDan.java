
public class WhileGuGuDan {

	public static void main(String[] args) {
		
	int i=2;
	while(i<=9) {
		int j =1;
		while(j<=9) {			
			System.out.printf("%d*%d=%d\t",i,j,i*j);
			//System.out.print(i +"*"+j +"=" +(i*j)+ " ");
			j++;
		}
		System.out.println();
		i++;
				
		
	}
		
	
	}//end

}//end
